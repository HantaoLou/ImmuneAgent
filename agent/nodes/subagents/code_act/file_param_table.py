def create_file_param_from_user_input(
    key: str,
    path: str,
    description: str,
    file_type: Optional[str] = None,
    metadata: Optional[Dict[str, Any]] = None
) -> FileParameter:
    """Create a FileParameter from user-provided file"""
    if file_type is None:
        file_type = path.split(".")[-1] if "." in path else "unknown"
    
    return FileParameter(
        key=key,
        path=path,
        description=description,
        source=FileSource.USER_INPUT,
        file_type=file_type,
        metadata=metadata or {}
    )


def create_file_param_from_task_output(
    key: str,
    path: str,
    description: str,
    task_id: str,
    file_type: Optional[str] = None,
    metadata: Optional[Dict[str, Any]] = None
) -> FileParameter:
    """Create a FileParameter from task output"""
    if file_type is None:
        file_type = path.split(".")[-1] if "." in path else "unknown"
    
    return FileParameter(
        key=key,
        path=path,
        description=description,
        source=FileSource.TASK_OUTPUT,
        source_task_id=task_id,
        file_type=file_type,
        metadata=metadata or {}
    )


def extract_file_info_from_task_result(
    task_id: str,
    task_description: str,
    task_result: Dict[str, Any]
) -> List[FileParameter]:
    """
    Extract file parameters from a completed task's result
    
    Args:
        task_id: ID of the completed task
        task_description: Description of the task
        task_result: Result dictionary from task execution
    
    Returns:
        List of FileParameter objects extracted from the result
    """
    file_params = []
    
    if not task_result:
        return file_params
    
    # Common output file keys to look for
    output_keys = [
        "output_file", "output_path", "result_file", "result_path",
        "predictions_file", "predictions_path", "predictions_csv",
        "integrated_file", "integrated_rds", "airr_file", "airr_path"
    ]
    
    for key in output_keys:
        if key in task_result:
            path = task_result[key]
            if isinstance(path, str) and path:
                # Extract file type
                file_type = path.split(".")[-1] if "." in path else "unknown"
                
                # Generate description based on task and file type
                description = f"Output from task '{task_description[:50]}'"
                if file_type == "csv":
                    description += " (CSV data file)"
                elif file_type == "rds":
                    description += " (RDS data file)"
                elif file_type == "fasta":
                    description += " (FASTA sequence file)"
                elif file_type == "tsv":
                    description += " (TSV data file)"
                
                file_param = create_file_param_from_task_output(
                    key=f"{task_id}_{key}",
                    path=path,
                    description=description,
                    task_id=task_id,
                    file_type=file_type
                )
                file_params.append(file_param)
    
    # Also check for nested output structure
    if "output" in task_result and isinstance(task_result["output"], dict):
        output = task_result["output"]
        for key, value in output.items():
            if isinstance(value, str) and ("/" in value or "\\" in value):
                # This looks like a file path
                file_type = value.split(".")[-1] if "." in value else "unknown"
                file_param = create_file_param_from_task_output(
                    key=f"{task_id}_{key}",
                    path=value,
                    description=f"Output '{key}' from task '{task_description[:30]}'",
                    task_id=task_id,
                    file_type=file_type
                )
                file_params.append(file_param)
    
    return file_params


