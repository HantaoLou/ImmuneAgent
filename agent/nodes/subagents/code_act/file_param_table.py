"""
File Parameter Table for CodeAct Dynamic Parameter Inference

This module provides:
1. FileParameter: A single file entry with path and description
2. FileParameterTable: A collection of file parameters with management functions

The file parameter table is used by CodeAct to:
- Store known files (from user input, task outputs)
- Provide context for LLM parameter inference
- Track file provenance (which task produced which file)
"""

from typing import Dict, List, Any, Optional
from pydantic import BaseModel, Field, ConfigDict
from enum import Enum
from datetime import datetime
import json


class FileSource(str, Enum):
    """Source of the file parameter"""
    USER_INPUT = "user_input"       # File provided by user
    TASK_OUTPUT = "task_output"     # File generated by a task
    SYSTEM = "system"               # System file (e.g., benchmark data)


class FileParameter(BaseModel):
    """
    A single file parameter entry with metadata
    
    Attributes:
        key: Unique identifier for this file (e.g., "predictions_csv", "integrated_rds")
        path: Absolute file path
        description: Human-readable description of file contents
        source: Where this file came from (user_input, task_output, system)
        source_task_id: If task_output, which task produced this file
        file_type: File extension/type (csv, rds, fasta, etc.)
        created_at: When this entry was created
        metadata: Additional metadata (columns, row count, etc.)
    """
    model_config = ConfigDict(
        use_enum_values=True,
        validate_assignment=True
    )
    
    key: str = Field(description="Unique identifier for this file parameter")
    path: str = Field(description="Absolute file path")
    description: str = Field(description="Human-readable description of file contents")
    source: FileSource = Field(description="Source of the file")
    source_task_id: Optional[str] = Field(default=None, description="Task ID that produced this file")
    file_type: Optional[str] = Field(default=None, description="File type/extension")
    created_at: str = Field(default_factory=lambda: datetime.now().isoformat())
    metadata: Dict[str, Any] = Field(default_factory=dict, description="Additional metadata")


class FileParameterTable(BaseModel):
    """
    Collection of file parameters for a session
    
    This table is:
    - Initialized with user-provided files
    - Updated after each task execution with output files
    - Used by infer_parameters_node to determine parameter values
    
    Attributes:
        session_id: Session identifier
        files: Dictionary mapping key -> FileParameter
        updated_at: Last update timestamp
    """
    model_config = ConfigDict(
        validate_assignment=True
    )
    
    session_id: str = Field(description="Session identifier")
    files: Dict[str, FileParameter] = Field(default_factory=dict)
    updated_at: str = Field(default_factory=lambda: datetime.now().isoformat())
    
    def add_file(self, file_param: FileParameter) -> None:
        """Add or update a file parameter"""
        self.files[file_param.key] = file_param
        self.updated_at = datetime.now().isoformat()
    
    def get_file(self, key: str) -> Optional[FileParameter]:
        """Get a file parameter by key"""
        return self.files.get(key)
    
    def get_file_by_path(self, path: str) -> Optional[FileParameter]:
        """Get a file parameter by path"""
        for f in self.files.values():
            if f.path == path:
                return f
        return None
    
    def get_files_by_type(self, file_type: str) -> List[FileParameter]:
        """Get all files of a specific type"""
        return [f for f in self.files.values() if f.file_type == file_type]
    
    def get_files_by_source(self, source: FileSource) -> List[FileParameter]:
        """Get all files from a specific source"""
        return [f for f in self.files.values() if f.source == source]
    
    def get_files_from_task(self, task_id: str) -> List[FileParameter]:
        """Get all files produced by a specific task"""
        return [f for f in self.files.values() if f.source_task_id == task_id]
    
    def to_context_string(self) -> str:
        """
        Generate a human-readable string for LLM context
        
        Returns:
            Formatted string describing all available files
        """
        if not self.files:
            return "No files available in the parameter table."
        
        lines = ["Available files in the session:"]
        for key, f in self.files.items():
            source_info = f.source
            if f.source_task_id:
                source_info += f" (from task: {f.source_task_id})"
            
            lines.append(f"  - [{key}] {f.path}")
            lines.append(f"    Type: {f.file_type or 'unknown'}")
            lines.append(f"    Source: {source_info}")
            lines.append(f"    Description: {f.description}")
            
            # Include metadata if available
            if f.metadata:
                for meta_key, meta_val in f.metadata.items():
                    lines.append(f"    {meta_key}: {meta_val}")
        
        return "\n".join(lines)
    
    def to_markdown(self) -> str:
        """Generate markdown representation for todo-list.md"""
        if not self.files:
            return "## File Parameter Table\n\nNo files registered.\n"
        
        lines = [
            "## File Parameter Table",
            "",
            "| Key | Path | Type | Source | Description |",
            "|-----|------|------|--------|-------------|",
        ]
        
        for key, f in self.files.items():
            source = f.source
            if f.source_task_id:
                source += f" ({f.source_task_id})"
            lines.append(f"| {key} | {f.path} | {f.file_type or '-'} | {source} | {f.description} |")
        
        return "\n".join(lines) + "\n"
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for serialization"""
        return {
            "session_id": self.session_id,
            "updated_at": self.updated_at,
            "files": {k: v.model_dump() for k, v in self.files.items()}
        }
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "FileParameterTable":
        """Create from dictionary"""
        files = {}
        for k, v in data.get("files", {}).items():
            files[k] = FileParameter(**v)
        
        return cls(
            session_id=data.get("session_id", "unknown"),
            updated_at=data.get("updated_at", datetime.now().isoformat()),
            files=files
        )


