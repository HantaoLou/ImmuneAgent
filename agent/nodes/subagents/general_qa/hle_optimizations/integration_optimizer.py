# ==================== Part 1: Success Evaluation Fix ====================

@dataclass
class EvaluationResult:
    """Result of evaluating a test answer"""
    is_success: bool
    true_success: bool  # True success (answer matches expected)
    is_timeout_recovery: bool  # Was in timeout recovery mode
    is_system_error: bool  # Was a system error
    error_type: Optional[str] = None
    error_message: Optional[str] = None
    final_answer: Optional[str] = None
    expected_answer: Optional[str] = None
    answer_matches: bool = False


def is_timeout_recovery(final_answer: Optional[str]) -> bool:
    """
    Check if the final answer is a timeout recovery placeholder.
    
    Timeout recovery messages should NOT be considered successful answers.
    """
    if final_answer is None:
        return False
    
    answer_str = str(final_answer).lower()
    
    timeout_indicators = [
        "inference could not be completed due to llm timeout",
        "recovery mode: partial",
        "timeout recovery",
        "llm call failed",
        "could not be completed",
        "timed out",
        "timeout occurred"
    ]
    
    return any(indicator in answer_str for indicator in timeout_indicators)


def classify_error(error_message: Optional[str]) -> str:
    """Classify error type from error message"""
    if not error_message:
        return "unknown"
    
    error_lower = str(error_message).lower()
    
    if "timeout" in error_lower:
        return "timeout"
    elif "llm" in error_lower or "api" in error_lower:
        return "llm_failure"
    elif "attribute" in error_lower or "type" in error_lower:
        return "code_bug"
    elif "parse" in error_lower:
        return "parse_error"
    elif "knowledge" in error_lower:
        return "knowledge_retrieval_failure"
    else:
        return "unknown"


def is_result_successful(
    final_answer: Optional[str],
    expected_answer: Optional[str] = None,
    error_message: Optional[str] = None,
    success_flag: bool = True  # Original success flag from system
) -> EvaluationResult:
    """
    Evaluate if a result is truly successful.
    
    This function fixes the bug where timeout recovery mode was incorrectly
    marked as success=True.
    
    Args:
        final_answer: The answer generated by the system
        expected_answer: The expected correct answer (optional)
        error_message: Any error message from the system
        success_flag: The original success flag from the system
        
    Returns:
        EvaluationResult with detailed success information
    """
    # Check for timeout recovery mode
    is_timeout = is_timeout_recovery(final_answer)
    
    # Check for system errors
    is_system_error = error_message is not None and len(str(error_message)) > 0
    error_type = classify_error(error_message) if is_system_error else None
    
    # Check if answer is None or empty
    is_empty_answer = final_answer is None or len(str(final_answer).strip()) == 0
    
    # Check answer match if expected answer provided
    answer_matches = False
    if expected_answer and final_answer:
        answer_matches = _check_answer_match(final_answer, expected_answer)
    
    # Determine true success
    # SUCCESS requires:
    # 1. Not in timeout recovery mode
    # 2. No system errors (unless it's a non-blocking warning)
    # 3. Has a valid answer
    # 4. Answer matches expected (if provided)
    
    true_success = True
    failure_reasons = []
    
    if is_timeout:
        true_success = False
        failure_reasons.append("timeout_recovery")
    
    if is_system_error and error_type not in ["unknown"]:
        true_success = False
        failure_reasons.append(f"system_error:{error_type}")
    
    if is_empty_answer:
        true_success = False
        failure_reasons.append("empty_answer")
    
    if expected_answer and not answer_matches:
        true_success = False
        failure_reasons.append("answer_mismatch")
    
    return EvaluationResult(
        is_success=success_flag and true_success,
        true_success=true_success,
        is_timeout_recovery=is_timeout,
        is_system_error=is_system_error,
        error_type=error_type,
        error_message=error_message,
        final_answer=final_answer,
        expected_answer=expected_answer,
        answer_matches=answer_matches
    )


def _check_answer_match(generated: str, expected: str) -> bool:
    """Check if generated answer matches expected answer"""
    gen_clean = str(generated).strip().upper()
    exp_clean = str(expected).strip().upper()
    
    # Direct match
    if gen_clean == exp_clean:
        return True
    
    # Extract single letter answer (for multiple choice)
    gen_letter = re.search(r'\b([A-H])\b', gen_clean)
    exp_letter = re.search(r'\b([A-H])\b', exp_clean)
    if gen_letter and exp_letter:
        if gen_letter.group(1) == exp_letter.group(1):
            return True
    
    # True/False match
    if gen_clean in ["TRUE", "FALSE"] and exp_clean in ["TRUE", "FALSE"]:
        return gen_clean == exp_clean
    
    # Numeric match (with some tolerance)
    gen_num = re.search(r'[-+]?\d*\.?\d+', gen_clean)
    exp_num = re.search(r'[-+]?\d*\.?\d+', exp_clean)
    if gen_num and exp_num:
        try:
            if abs(float(gen_num.group()) - float(exp_num.group())) < 0.01:
                return True
        except ValueError:
            pass
    
    # Partial match for text answers
    if exp_clean in gen_clean or gen_clean in exp_clean:
        return True
    
    return False


# ==================== Part 2: Adaptive Timeout Strategy ====================

class ComplexityLevel(Enum):
    """Complexity levels for questions"""
    SIMPLE = "simple"
    MODERATE = "moderate"
    COMPLEX = "complex"
    VERY_COMPLEX = "very_complex"
    HLE_LEVEL = "hle_level"


# Default timeouts by complexity level (in seconds)
COMPLEXITY_TIMEOUTS = {
    ComplexityLevel.SIMPLE: 60,
    ComplexityLevel.MODERATE: 180,
    ComplexityLevel.COMPLEX: 300,
    ComplexityLevel.VERY_COMPLEX: 600,
    ComplexityLevel.HLE_LEVEL: 900
}

# Node-specific timeout adjustments
NODE_TIMEOUT_ADJUSTMENTS = {
    "n0_input_preprocessing": 30,
    "n1_question_decomposition": 90,
    "n2_calculation_algorithm_recognition": 60,
    "n3_knowledge_retrieval": 180,
    "n4_calculation_decomposition": 240,
    "n6_initial_inference": 180,
    "n7_complete_inference": 300,
    "n8_answer_generation": 120,
    "n10_exception_handling": 60
}


def estimate_complexity(question: str, question_type: Optional[str] = None) -> ComplexityLevel:
    """
    Estimate the complexity level of a question.
    
    Uses multiple heuristics to determine how much time a question might need.
    """
    score = 0
    
    # Length factor
    if len(question) > 500:
        score += 1
    if len(question) > 1000:
        score += 1
    if len(question) > 2000:
        score += 1
    
    # Multi-step indicators
    multi_step_patterns = [
        r"step[s]?\s+\d+", r"first[,\.].*then", r"after.*before",
        r"calculate.*explain", r"determine.*verify"
    ]
    if any(re.search(p, question, re.IGNORECASE) for p in multi_step_patterns):
        score += 1
    
    # Calculation indicators
    calc_patterns = [
        r"calculate|compute|determine", r"what (is|are) the (ratio|percentage)",
        r"how many|how much", r"frequency|probability"
    ]
    if any(re.search(p, question, re.IGNORECASE) for p in calc_patterns):
        score += 1
    
    # Domain complexity
    domain_keywords = {
        "genetics": ["gene", "allele", "genotype", "phenotype", "inheritance"],
        "molecular_biology": ["protein", "rna", "dna", "transcription", "pathway"],
        "clinical": ["diagnosis", "treatment", "patient", "symptom", "therapy"],
        "bioinformatics": ["sequence", "alignment", "annotation", "variant"]
    }
    domains_detected = sum(
        1 for keywords in domain_keywords.values()
        if any(kw in question.lower() for kw in keywords)
    )
    score += min(domains_detected, 2)
    
    # Novel concepts
    if any(kw in question.lower() for kw in ["novel", "new", "recent", "hypothetical"]):
        score += 1
    
    # Map score to complexity level
    if score >= 7:
        return ComplexityLevel.HLE_LEVEL
    elif score >= 5:
        return ComplexityLevel.VERY_COMPLEX
    elif score >= 3:
        return ComplexityLevel.COMPLEX
    elif score >= 1:
        return ComplexityLevel.MODERATE
    else:
        return ComplexityLevel.SIMPLE


def get_adaptive_timeout(
    question: str,
    node_name: Optional[str] = None,
    question_type: Optional[str] = None
) -> float:
    """
    Get adaptive timeout for a question/node combination.
    
    Args:
        question: The question text
        node_name: Optional node name for node-specific adjustments
        question_type: Optional question type hint
        
    Returns:
        Timeout in seconds
    """
    # Get base complexity timeout
    complexity = estimate_complexity(question, question_type)
    base_timeout = COMPLEXITY_TIMEOUTS[complexity]
    
    # Apply node-specific adjustment
    if node_name and node_name in NODE_TIMEOUT_ADJUSTMENTS:
        node_timeout = NODE_TIMEOUT_ADJUSTMENTS[node_name]
        # Use weighted average of complexity-based and node-based
        final_timeout = (base_timeout * 0.6 + node_timeout * 0.4)
    else:
        final_timeout = base_timeout
    
    # Ensure minimum of 60 seconds and maximum of 1200 seconds
    return max(60, min(1200, final_timeout))


