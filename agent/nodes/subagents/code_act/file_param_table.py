def get_parameter_inference_prompt(
    tool_name: str,
    tool_description: str,
    tool_parameters: List[Dict[str, Any]],
    user_input: str,
    file_param_table: FileParameterTable,
    completed_tasks: List[Dict[str, Any]]
) -> str:
    """
    Generate LLM prompt for parameter inference
    
    Args:
        tool_name: Name of the tool to call
        tool_description: Description of the tool
        tool_parameters: List of parameter definitions
        user_input: Original user input
        file_param_table: Current file parameter table
        completed_tasks: List of completed task summaries
    
    Returns:
        Formatted prompt string for LLM
    """
    # Build file context
    file_context = file_param_table.to_context_string()
    
    # Build completed tasks context
    tasks_context = "Recently completed tasks:"
    if completed_tasks:
        for task in completed_tasks[-5:]:  # Last 5 tasks
            tasks_context += f"\n  - {task.get('id', 'unknown')}: {task.get('description', 'N/A')[:50]}"
            if task.get('result_summary'):
                tasks_context += f"\n    Result: {task['result_summary'][:100]}"
    else:
        tasks_context += "\n  No tasks completed yet."
    
    # Build parameter definitions
    params_context = "Tool parameters to fill:"
    for param in tool_parameters:
        param_name = param.get("name", "unknown")
        param_type = param.get("type", "any")
        param_desc = param.get("description", "")
        required = param.get("required", False)
        
        params_context += f"\n  - {param_name} ({param_type})"
        if required:
            params_context += " [REQUIRED]"
        if param_desc:
            params_context += f": {param_desc}"
    
    prompt = f"""You are a parameter inference expert. Your task is to determine the correct parameter values for a tool call based on available context.

# Tool Information
- Tool name: {tool_name}
- Description: {tool_description}

# Parameter Definitions
{params_context}

# Available Context

## User's Original Request
{user_input}

## {file_context}

## {tasks_context}

# Your Task
Analyze the above context and determine the appropriate parameter values for this tool call.

Rules:
1. For file parameters, use the exact file path from the File Parameter Table
2. If a parameter should come from a task output, reference the appropriate file from the table
3. If a required parameter cannot be determined, mark it as "NEEDS_USER_INPUT"
4. Only include parameters that have values to fill

# Output Format
Return a JSON object with the inferred parameters:
```json
{{
  "parameters": {{
    "param_name": "inferred_value",
    ...
  }},
  "inference_notes": "Brief explanation of how parameters were determined"
}}
```

Return only the JSON object, no additional text."""

    return prompt


