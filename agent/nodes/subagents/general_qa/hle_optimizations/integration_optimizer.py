# ==================== Part 4: Integration Optimizer ====================

class IntegrationOptimizer:
    """
    Main optimizer class that integrates all optimization strategies.
    
    Usage:
        optimizer = IntegrationOptimizer()
        
        # Check if result is successful
        result = optimizer.evaluate_success(
            final_answer=answer,
            expected_answer=expected,
            error_message=error
        )
        
        # Get adaptive timeout
        timeout = optimizer.get_timeout(question, node_name="n7_complete_inference")
        
        # Get domain template
        template = optimizer.get_template("genetics", "H2_vs_h2")
    """
    
    def __init__(
        self,
        custom_timeouts: Optional[Dict[str, float]] = None,
        enable_logging: bool = True
    ):
        self.custom_timeouts = custom_timeouts or {}
        self.enable_logging = enable_logging
        self._evaluation_history: List[EvaluationResult] = []
        self._timeout_history: List[Dict[str, Any]] = []
    
    def evaluate_success(
        self,
        final_answer: Optional[str],
        expected_answer: Optional[str] = None,
        error_message: Optional[str] = None,
        success_flag: bool = True
    ) -> EvaluationResult:
        """Evaluate if a result is truly successful"""
        result = is_result_successful(
            final_answer=final_answer,
            expected_answer=expected_answer,
            error_message=error_message,
            success_flag=success_flag
        )
        
        self._evaluation_history.append(result)
        
        if self.enable_logging and not result.true_success:
            logger.info(
                f"Result evaluation: success={result.true_success}, "
                f"timeout={result.is_timeout_recovery}, "
                f"error_type={result.error_type}"
            )
        
        return result
    
    def get_timeout(
        self,
        question: str,
        node_name: Optional[str] = None,
        question_type: Optional[str] = None
    ) -> float:
        """Get adaptive timeout for a question"""
        timeout = get_adaptive_timeout(question, node_name, question_type)
        
        # Apply custom overrides
        if node_name and node_name in self.custom_timeouts:
            timeout = self.custom_timeouts[node_name]
        
        self._timeout_history.append({
            "timestamp": datetime.now().isoformat(),
            "question_length": len(question),
            "node_name": node_name,
            "timeout": timeout
        })
        
        if self.enable_logging:
            logger.debug(f"Timeout allocated: {timeout}s for {node_name or 'general'}")
        
        return timeout
    
    def get_template(
        self,
        domain: str,
        topic: Optional[str] = None
    ) -> Dict[str, Any]:
        """Get domain reasoning template"""
        return get_domain_reasoning_template(domain, topic)
    
    def get_statistics(self) -> Dict[str, Any]:
        """Get statistics about optimizer usage"""
        stats = {
            "total_evaluations": len(self._evaluation_history),
            "true_success_count": sum(1 for e in self._evaluation_history if e.true_success),
            "timeout_recovery_count": sum(1 for e in self._evaluation_history if e.is_timeout_recovery),
            "system_error_count": sum(1 for e in self._evaluation_history if e.is_system_error),
            "total_timeout_allocations": len(self._timeout_history),
            "average_timeout": (
                sum(t["timeout"] for t in self._timeout_history) / len(self._timeout_history)
                if self._timeout_history else 0
            )
        }
        
        if stats["total_evaluations"] > 0:
            stats["true_success_rate"] = stats["true_success_count"] / stats["total_evaluations"]
        
        return stats
    
    def generate_report(self) -> str:
        """Generate a summary report of optimizer usage"""
        stats = self.get_statistics()
        
        report = f"""
# Integration Optimizer Report

## Evaluation Statistics
- Total Evaluations: {stats['total_evaluations']}
- True Success: {stats['true_success_count']} ({stats.get('true_success_rate', 0)*100:.1f}%)
- Timeout Recovery: {stats['timeout_recovery_count']}
- System Errors: {stats['system_error_count']}

## Timeout Statistics
- Total Allocations: {stats['total_timeout_allocations']}
- Average Timeout: {stats['average_timeout']:.1f}s

## Impact Analysis
- Bug Fixes Applied: Success evaluation now correctly identifies timeout recovery
- Optimizations Active: Adaptive timeouts, domain templates
"""
        return report


# ==================== Convenience Functions ====================

_global_optimizer: Optional[IntegrationOptimizer] = None


def get_global_optimizer() -> IntegrationOptimizer:
    """Get or create the global optimizer instance"""
    global _global_optimizer
    if _global_optimizer is None:
        _global_optimizer = IntegrationOptimizer()
    return _global_optimizer


def quick_evaluate(
    final_answer: Optional[str],
    expected_answer: Optional[str] = None,
    error_message: Optional[str] = None
) -> bool:
    """Quick evaluation function for simple use cases"""
    result = is_result_successful(
        final_answer=final_answer,
        expected_answer=expected_answer,
        error_message=error_message
    )
    return result.true_success


